# PLOT 2: Parallel Performance Speedup
# =========================================================================
def generate_exp2_plots():
    input_files = {
        "App 1 (Deferred)": "Experiment_02_Approach1.txt",
        "App 2 (Immediate)": "Experiment_02_Approach2.txt",
        "Baseline (No Insert/Delete)": "Experiment_02_no_insert_delete.txt",
    }
    loaded_data = {}
    for tag, filepath in input_files.items():
        if os.path.exists(filepath):
            parsed = load_csv_data(filepath)
            if parsed is not None:
                loaded_data[tag] = parsed

    if not loaded_data:
        print("  [SKIP] Exp 2 files are missing.")
        return

    reference_data = list(loaded_data.values())[0]
    unique_grids = sorted(set([(int(row[0]), int(row[1])) for row in reference_data]))

    colors = ['crimson', 'teal', 'darkgoldenrod']
    markers = ['o', 'v', 'p']

    for grid_x, grid_y in unique_grids:
        plt.figure(figsize=(9, 6))
        for idx, (tag, data_arr) in enumerate(loaded_data.items()):
            cond = (data_arr[:, 0] == grid_x) & (data_arr[:, 1] == grid_y)
            subset = data_arr[cond]
            if len(subset) == 0:
                continue
            
            thread_counts = subset[:, 2].astype(int)
            speedup_vals = subset[:, 5]
            
            plt.plot(thread_counts, speedup_vals, marker=markers[idx % len(markers)], 
                     color=colors[idx % len(colors)], linestyle='-', linewidth=2, markersize=8, label=tag)
        
        plt.plot([2, 4, 8, 16], [2, 4, 8, 16], color='gray', linestyle='-.', alpha=0.6, label='Ideal Scaling')
        plt.xlabel('Thread Count', fontsize=12)
        plt.ylabel('Speedup Ratio', fontsize=12)
        plt.title(f'Experiment 2: Parallel Speedup ({grid_x}×{grid_y}, 14M pts)', fontsize=14)
        plt.legend(fontsize=11)
        plt.grid(True, linestyle='--', alpha=0.4)
        plt.xticks([2, 4, 8, 16])
        plt.tight_layout()
        plt.savefig(f"{PLOT_OUTPUT_DIR}/exp2_parallel_speedup_{grid_x}x{grid_y}.png", dpi=150)
        plt.close()
        print(f"  Exported exp2_parallel_speedup_{grid_x}x{grid_y}.png")

