import os
import sys
import numpy as np
import matplotlib
matplotlib.use('Agg')
import matplotlib.pyplot as plt

PLOT_OUTPUT_DIR = "generated_plots"
os.makedirs(PLOT_OUTPUT_DIR, exist_ok=True)

def load_csv_data(filepath, skip_lines=2):
    """Reads CSV data from experiment logs, bypassing headers."""
    parsed_data = []
    for encoding_type in ['utf-16', 'utf-8', 'latin-1']:
        try:
            with open(filepath, encoding=encoding_type) as file_obj:
                file_lines = file_obj.readlines()
            break
        except (UnicodeError, UnicodeDecodeError):
            continue
    else:
        print(f"  [ERROR] Failed to read {filepath}")
        return None
    
    for row in file_lines[skip_lines:]:
        row = row.strip()
        if not row or 'OOM' in row:
            continue
        row_elements = row.split(',')
        try:
            parsed_data.append([float(val) for val in row_elements])
        except ValueError:
            continue
    return np.array(parsed_data) if parsed_data else None

