# PLOT 4: Per-Particle Execution Cost (PPC)
# =========================================================================
def generate_ppc_plots():
    for method_name, file_name in [("Approach1", "Experiment_01_Approach1.txt"),
                                   ("Approach2", "Experiment_01_Approach2.txt")]:
        if not os.path.exists(file_name):
            continue
        
        dataset = load_csv_data(file_name)
        if dataset is None:
            continue
        
        unique_grids = sorted(set([(int(row[0]), int(row[1])) for row in dataset]))
        plt.figure(figsize=(9, 6))
        
        for idx, (grid_x, grid_y) in enumerate(unique_grids):
            cond = (dataset[:, 0] == grid_x) & (dataset[:, 1] == grid_y)
            subset = dataset[cond]
            
            pts_per_cell = subset[:, 2] / (grid_x * grid_y)
            time_per_pt = subset[:, 5] / (subset[:, 2] * 10) 
            
            markers = ['o', '^', 's']
            plt.loglog(pts_per_cell, time_per_pt, marker=markers[idx % 3], linestyle='-', 
                       linewidth=2, markersize=7, label=f'Grid: {grid_x}×{grid_y}')
            
        plt.xlabel('Particles Per Cell (PPC)', fontsize=12)
        plt.ylabel('Runtime per Particle (Seconds)', fontsize=12)
        plt.title(f'Efficiency: Time per Particle vs PPC ({method_name})', fontsize=14)
        plt.legend(fontsize=11)
        plt.grid(True, which='both', linestyle=':', alpha=0.5)
        plt.tight_layout()
        plt.savefig(f"{PLOT_OUTPUT_DIR}/efficiency_ppc_{method_name}.png", dpi=150)
        plt.close()
        print(f"  Exported efficiency_ppc_{method_name}.png")

