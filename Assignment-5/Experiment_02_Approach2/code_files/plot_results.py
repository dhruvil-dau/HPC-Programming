# PLOT 3: Spatial Distribution Analysis
# =========================================================================
def generate_distribution_plots():
    file_configs = [
        ("positions_initial.csv", "Initial State"),
        ("positions_after_Approach1.csv", "Post-Approach 1 Mover"),
        ("positions_after_Approach2.csv", "Post-Approach 2 Mover")
    ]
    
    for file_path, plot_title in file_configs:
        if not os.path.exists(file_path):
            print(f"  [SKIP] Could not find {file_path}")
            continue
        
        csv_data = np.genfromtxt(file_path, delimiter=',', skip_header=1)
        x_coords, y_coords = csv_data[:, 0], csv_data[:, 1]

        fig, subplots = plt.subplots(1, 3, figsize=(16, 5))

        sample_size = min(5000, len(x_coords))
        sample_indices = np.random.choice(len(x_coords), sample_size, replace=False)
        
        subplots[0].scatter(x_coords[sample_indices], y_coords[sample_indices], s=1.0, alpha=0.6, color='indigo')
        subplots[0].set_xlim(0, 1)
        subplots[0].set_ylim(0, 1)
        subplots[0].set_title(f'Particle Scatter – {plot_title}')
        subplots[0].set_xlabel('X-Axis')
        subplots[0].set_ylabel('Y-Axis')
        subplots[0].set_aspect('equal')

        subplots[1].hist(x_coords, bins=50, alpha=0.75, color='mediumseagreen', edgecolor='white')
        subplots[1].set_title(f'X-Coord Histogram')
        subplots[1].set_xlabel('X Value')

        subplots[2].hist(y_coords, bins=50, alpha=0.75, color='tomato', edgecolor='white')
        subplots[2].set_title(f'Y-Coord Histogram')
        subplots[2].set_xlabel('Y Value')

        plt.tight_layout()
        save_tag = file_path.replace('.csv', '').replace('positions_', '')
        plt.savefig(f"{PLOT_OUTPUT_DIR}/dist_verify_{save_tag}.png", dpi=150)
        plt.close()
        print(f"  Exported dist_verify_{save_tag}.png")

