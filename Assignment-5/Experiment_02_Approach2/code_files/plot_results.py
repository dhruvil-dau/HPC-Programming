# HARDWARE TABLE
# =========================================================================
def display_hardware_metrics():
    print("\n=== Hardware Metrics (Memory & FLOPs) ===")
    print(f"{'NX':>6} {'NY':>6} {'Num_Pts':>12} {'Pts_RAM(MB)':>12} "
          f"{'Grid_RAM(KB)':>12} {'Total_RAM(MB)':>14} {'GFLOPs':>10} {'PPC':>10}")
    grid_sizes = [(250, 100), (500, 200), (1000, 400)]
    pt_counts = [100, 10000, 1000000, 100000000, 1000000000]
    iterations = 10
    
    for g_x, g_y in grid_sizes:
        for pts in pt_counts:
            mem_pts = pts * 16 
            mem_grid = (g_x + 1) * (g_y + 1) * 8
            total_mem = (mem_pts + mem_grid) / 1e6
            total_flops = pts * iterations * 34
            density = pts / (g_x * g_y)
            
            print(f"{g_x:>6} {g_y:>6} {pts:>12} {mem_pts/1e6:>12.2f} "
                  f"{mem_grid/1e3:>12.2f} {total_mem:>14.2f} "
                  f"{total_flops/1e9:>10.3f} {density:>10.1f}")

