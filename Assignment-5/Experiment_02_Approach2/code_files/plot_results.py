# PLOT 1: Execution Runtime vs Particle Count
# =========================================================================
def generate_exp1_plots():
    for method_name, file_name in [("Approach1", "Experiment_01_Approach1.txt"),
                                   ("Approach2", "Experiment_01_Approach2.txt")]:
        if not os.path.exists(file_name):
            print(f"  [SKIP] Missing {file_name}")
            continue
        
        dataset = load_csv_data(file_name)
        if dataset is None:
            continue
        
        unique_grids = sorted(set([(int(row[0]), int(row[1])) for row in dataset]))
        for grid_x, grid_y in unique_grids:
            condition = (dataset[:, 0] == grid_x) & (dataset[:, 1] == grid_y)
            filtered_data = dataset[condition]
            num_particles = filtered_data[:, 2]
            total_time = filtered_data[:, 5]
            
            plt.figure(figsize=(8, 6))
            plt.loglog(num_particles, total_time, marker='^', linestyle='-', linewidth=2.5, markersize=7,
                       label=f'{method_name} ({grid_x}x{grid_y})', color='tab:blue')
            plt.xlabel('Particle Count', fontsize=12)
            plt.ylabel('Runtime (Seconds)', fontsize=12)
            plt.title(f'Experiment 1: {method_name} Scaling on {grid_x}×{grid_y}', fontsize=14)
            plt.legend(fontsize=11)
            plt.grid(True, which='both', linestyle='--', alpha=0.4)
            plt.tight_layout()
            plt.savefig(f"{PLOT_OUTPUT_DIR}/exp1_{method_name}_{grid_x}x{grid_y}.png", dpi=150)
            plt.close()
            print(f"  Exported exp1_{method_name}_{grid_x}x{grid_y}.png")

    data_app1 = load_csv_data("Experiment_01_Approach1.txt") if os.path.exists("Experiment_01_Approach1.txt") else None
    data_app2 = load_csv_data("Experiment_01_Approach2.txt") if os.path.exists("Experiment_01_Approach2.txt") else None
    
    if data_app1 is not None and data_app2 is not None:
        combined_grids = sorted(set([(int(r[0]), int(r[1])) for r in data_app1]))
        for grid_x, grid_y in combined_grids:
            plt.figure(figsize=(9, 6))
            
            plot_configs = [
                ("Deferred (Approach 1)", data_app1, 's', '--', 'tab:purple'),
                ("Immediate (Approach 2)", data_app2, 'D', '-', 'tab:orange')
            ]
            
            for lbl, data_arr, mkr, ls, clr in plot_configs:
                cond = (data_arr[:, 0] == grid_x) & (data_arr[:, 1] == grid_y)
                subset = data_arr[cond]
                plt.loglog(subset[:, 2], subset[:, 5], marker=mkr, linestyle=ls, color=clr, 
                           linewidth=2.5, markersize=7, label=lbl)
            
            plt.xlabel('Particle Count', fontsize=12)
            plt.ylabel('Runtime (Seconds)', fontsize=12)
            plt.title(f'Comparison: Approach 1 vs 2 for {grid_x}×{grid_y}', fontsize=14)
            plt.legend(fontsize=11)
            plt.grid(True, which='both', linestyle=':', alpha=0.5)
            plt.tight_layout()
            plt.savefig(f"{PLOT_OUTPUT_DIR}/exp1_comparison_{grid_x}x{grid_y}.png", dpi=150)
            plt.close()
            print(f"  Exported exp1_comparison_{grid_x}x{grid_y}.png")

